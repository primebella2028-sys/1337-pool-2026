#include <unistd.h>


int	main(int argc, char **argv)
{
	int	i;
	int	j;

	j = 0;
	i = 1;
	if (argc != 2)
	{
		write (1, "a\n", 2);
		return (0);
	}
	while (argv[i][j])
	{
		if (argv[i][j] == 'a')
		{
			write(1, "a", 1);
			write(1, "\n", 1);
			return (0);
		}
		j++;
	} 
		write(1, "\n", 1);
}


