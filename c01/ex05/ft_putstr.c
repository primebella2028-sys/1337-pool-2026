#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
	{
		write(1, str, 1);
		str++;
	}
}

int	main()
{
	char *ayoub = "ayoub bella is 1337 student";

	ft_putstr(ayoub);
	return (0);

}
